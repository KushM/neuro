h=0.002;
v = zeros (100,1)
v (1,1)=-0.07

for n=2:100
if (v(n-1,1)==0.060)
    v(n)=-0.070
else
    k1=-100.0*v(n-1)-7.0+11.0
    k2=-7.0-100.0*(v(n-1,1)+h*k1)+11.0
    v(n)=v(n-1,1)+0.5*h*(k1+k2)
    if (v(n,1)>0.020)
        v(n,1)=0.060
    end
end
end

t=0:0.002:0.1980
plot (t,v)
